                        <p class="text mt-2">Another important difference to consider between N and FL is the <i>density</i>, FL is denser than the water so it sinks, can be good in certain situations but not in others.
                            As you probably understood, FL is good for sinking lures, wet flies or nymph; N is better for topwater lures and dry flies.
                            Bear in mind this concept, it’s really important!
                        </p>
