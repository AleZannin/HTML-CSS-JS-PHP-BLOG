                        <h3 class="post_title text-center">FuoroCarbon Vs Nylon</h3>
                        <img src="img/tippets.jpg" class="img-fluid mt-3" alt="tippets" id="post-pics">
                        <p><i class="far fa-calendar-alt mt-4 ml-3" id="date"> 14th May 2019</i></p>
                        <p class="text mt-3">What is the best monofilament to use? In this post, I’ll simply explain what is the difference between fluorocarbon and nylon and when is better to use one than another. </p>
                        <p class="text mt-1">Nylon (N) and fluorocarbon (FL) are monofilaments. Monofilament, as the word says, is a continuous filament of synthetic fiber.
                            In 1938 the DuPout company announced the invention of nylon, during the 50s appear in the market the first synthetic fishing line. They were made of polyester, initially it was too elastic so after targeted studies, they improved into a monofilament.
                            We have to wait the 80s for the FL, it was invented in Japanese factories. They used it massively to build fishing nets, obtaining valid results to the point of being banned by the Japanese Ministry of the Environment.
                            Since then we can find it in Europe and US shops.
                            The FL main feature is very low visibility, almost like the water.
                            Both of them have pros and cons, factors to consider before getting ready your cast.</p>
