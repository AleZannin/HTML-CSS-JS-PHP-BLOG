It is really important to keep clean and dry your boots and waders, especially you move into different rivers. During the centuries the humans made enough disasters with the natural environment, be conscious of this, it’s really important. <br><br>

<i>Tip:</i> if you buy wading boots on-line get an extra size, for example, if your foot is size 10, buy size 11. In this way, you leave enough space for the waders between your foot and the water. I use an old pair of hand made wool socks between my waders and the boots to prevent scratching in the waders from the small pebbles. <br><br>

