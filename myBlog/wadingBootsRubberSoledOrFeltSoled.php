                        <h3 class="post_title text-center">Wading boots: <br> rubber-soled or felt-soled?</h3>
                        <img src="img/waderandboots.jpg" class="img-fluid mt-3" alt="boots" id="post-pics">
 <p><i class="far fa-calendar-alt mt-4 ml-3" id="date"> 23th June 2019</i></p>
                        <p class="text mt-3">Wading boots are an essential part of your equipment, they must give the right stability and comforts, sometimes you can wear them for several hours, so it’s really important to choose the proper boots. <br>
When you have to buy a pair of boots you have 2 choices, rubber-soled or felt-soled. <br><br>

<i>Felt-soled:</i> felt is a textile made by pressing fibers together, it has an excellent grip on the underwater stones clothed by silt or moss, but the felt is really slippery on the wet grass or wet logs. <br>
Felt-soled boots, like waders, can carry spores, this particular is quite important.
If you don’t clean and dry your boots and waders you can spread invasive species in other rivers. In some States, the felt-soled boots are banned. <br><br>

<i>Rubber-soled:</i> years ago the rubber-soled boots for fishing were really bad, both in quality and function: after a while, the sole came off and the grip was almost equal to zero. <br>
With the new technology, the rubber-soled are getting much better, the grip is almost the same, but the felt is still better underwater.
</p>
