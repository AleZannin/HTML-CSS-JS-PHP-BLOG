                        <p class="text mt-3">As we saw there are pros and cons in both soles. Your choice, after carefully checking the regulation, should depend on where you are planning to go and what kind of approach you have for the day. <br>
If you are going to spend all day in a few hundreds of meters of the river staying a lot of time in the water it is better to use felt-soled boots, they give you more stability and security inside the water. <br>
If you are planning to walk for some kilometers, fishing in some pool or hot spot, maybe climbing a mountain, it is 100% better to use rubber-soled boots.
</p>
                        <img src="img/attention.jpg" class="img-fluid mt-3" alt="attention" id="post-pics">
