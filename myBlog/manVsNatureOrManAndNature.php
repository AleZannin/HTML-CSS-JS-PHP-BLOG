                        <h3 class="post_title text-center">Fishing: man Vs nature or man and nature?</h3>
                        <img src="img/nature1.jpg" class="img-fluid mt-3" alt="nature" id="post-pics">
 <p><i class="far fa-calendar-alt mt-4 ml-3" id="date"> 16th June 2019</i></p>
                          <p class="text mt-3">When you start fishing you have just one goal: catch a fish! Your focus is all on that goal, then you want to catch a lot of fish! When you have a lot of nice pictures of different fish your next target is a big and beautiful fish! <br><br>
More or less this is the path for everybody, at the beginning. After a while, we raise our eyes
and we start to watch over the water flow and the flying insects, we begin to watch the wonderful world around us. <br>
If you stop a moment and you think about it you will realize how lucky we are, discover places where not many people go, some of them are almost exclusively for fishermen. <br>
One of the biggest pleasures of fishing is the relation with the nature, pure and simple. <br><br>
Unfortunately, the world where we live is passing the worst period of its history, everyone should be facing up with this problem. It’s time to do something. <br>
We are not just fishermen, we are water guardians, and, for this reason, we have a duty to protect this environment. <br> 
The last century is the worst of our history, we did more disasters in 100 years than in thousands of years.  We are destroying the world where we live, our first home, as humans, we are taking lots more than what we really need.</p>
