<p class="text mt-3">We are men Vs nature when we chase the fish, when we involve all our abilities to catch it.
We are men and nature when we began to enjoy this hobby with an eye on what is happening around us. <br>
Few good habits should be part of every one of us, like leave the spot as we find it, or even better. We shouldn’t throw our own rubbish, it’s really sad to find leaders, spools, cigarettes filters and so on along the river banks. <br>
A leader can be as well pretty danger during the wading, you can stumble on it and fall in the water. I pick them up every time. <br>
Carry a bag for pick up other rubbish can be a good idea as well, plastic takes decades or centuries to decompose, during this process it releases harmful substances into the earth and the water. It takes a few seconds for us to pick it up. <br><br> </p>
<img src="img/nature2.jpg" class="img-fluid" alt="ecology" id="post-pics">
<p class="text mt-3">
Pollution is making a huge disaster in aquatic life. When I was a child, back in the ’80s, where I grew (a lovely little town at the feet of the Alps in Italy), was plenty of fish. In the last years, the situation is totally changing. Pollution and some poachers are playing an important role in this. Most of us can’t do anything about it. Often, unfortunately, the industry and the business don’t care about the future of our globe. <br>
What we can do is to try to save some fish life, a well made catch & release can help to leave a better situation for the next generation. <br>
During all day, it doesn’t matter if we are at work, with the family, fishing or doing anything else. It’s time to think seriously about sou future, the future of our first home, the future of our only home. <br><br>
What about your area? How it is changing during the years? Share your story and your opinion about it in the comments section below.
</p>
