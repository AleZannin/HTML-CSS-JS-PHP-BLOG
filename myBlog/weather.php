                        <h4 class="under_title text-center mt-2">Oxygen</h4>
                        <p class="text mt-2">Oxygen is necessary for many forms of life including fish. Fish use oxygen in respiration, similar to organisms on land, and get oxygen through their gills.
                            Dissolved oxygen enters the water through the air or from the plants. The aeration of water can be caused by wind (creating waves), rapids, waterfalls, ground water discharge or other forms of running water. Dissolved oxygen is also produced as a waste product of photosynthesis from weed and other aquatic plants.
                            Cold water holds more oxygen than warm water, hot summer temperatures can heat water so it wonâ€™t hold enough oxygen to maintain fish life.
                            Dissolved oxygen levels can range from less than 1 mg/L to more than 20 mg/L depending on how all of these factors interact. In freshwater systems such as lakes, rivers and streams, dissolved oxygen concentrations will vary by season, location and water depth.
                            <span style="color:red">Fresh water fish prefers waters rich of oxygen</span> and tempt to avoid the areas where the oxygen level is less than 5mg/L.<br> Oxygen content, water temperature and barometric pressure go hand in hand.</p>
