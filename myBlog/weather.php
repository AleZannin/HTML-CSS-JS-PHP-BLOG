                        <h4 class="under_title text-center mt-3">Atmospheric Pressure</h4>
                        <p class="text mt-2">Amospheric pressure, is also knows as barometric pressure, is the force exerted by the atmosphere on the earth.
                            Whit<span style="color:red"> high pressure (H) the sky clear with generally bright light conditions. During this time, fish activity is generally slower, as the fish find cover or swim into deeper water. </span> This is a good moment for jigging or attractive baits. <br>
                            Instead,<span style="color:red"> with low pressure (L) the sky is overall cloudy and the fishing conditions are better, fish is generelly more active in the shallow water.</span>
                            If there is a high pressure area and a low pressure area, we will expect a movement of air from one to another area, this produce wind.
                    </div>
