                        <h4 class="under_title text-center mt-3">Conclusion</h4>
                        <p class="text mt-2">Choose the fishing trip based on the weather conditions can make a big difference in the final result.
                            Low pressure, some clouds and small wind from west can be really helpful, those are the best conditions for a fishing day because fish is more inclined to eat.<br>
                            It is very important to check the forecast the day before, in certain condition of bad weather the fishing experience is poor, besides can be as well really dangerous. Avoid any kind of fishing in a conditions of weather alert, especially if there are thunders and lightnings on the way.<br>
                            A good habit is to wear always a life jacket, better if is both manual and automatic, when we are near the water. Doesn't matter if is a stream, a river, a shore or a lake, sometime what can happen is out of our control. Safe first!
                        </p>
