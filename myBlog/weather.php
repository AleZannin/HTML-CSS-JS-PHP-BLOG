                        <h4 class="under_title text-center mt-3">Wind</h4>
                        <p class="text mt-1"><span style="color:red">Wind, if is moderate, can be a fishermanâ€™s friend</span>, the waves generate from the wind reduce the visibility underwarter, so the fish can't see properly the bite, moreover the wind can break out the food chain. You should cast in the same direction of the wind, fishing is easier and the bait moves with the wind. <br><br> An old proverb says:<br><br><b>Wind from the West, fish bite the best.<br>
                                Wind from the East, fish bite the least.<br>
                                Wind from the North, do not go forth.<br>
                                Wind from the South blows the bait in their mouth.<br><br></b>It is never wrong to listen what our forefathers said.</p>
