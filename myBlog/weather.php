                        <h4 class="under_title text-center mt-3">Rain</h4>
                        <p class="text mt-1">Even rain, like the wind, can be fisheraman friend. Like wind has to be moderate, <span style="color:red"> rain has to be light and steady to offer the best conditions</span>. Insects are inclined to move on the water surface during or after a light rain, this "irresistible food" bring the fish closest to the surface becoming more active. During the summer the water is warmer, and the oxygen gets low, with rain the water gets aerated. At this stage is easier to catch a nice fish. </p>
