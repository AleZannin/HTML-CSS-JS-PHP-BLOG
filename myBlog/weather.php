                        <h4 class="under_title text-center mt-3">Temperature</h4>
                        <p class="text mt-2">Fish are cold-blooded. This means that they don't maintain a constant internal body temperature;
                            instead, their temperature is influenced by their environment.
                            Very high or very low temperature limits the movement of fish.
                            In warm water, enzymes that digest the food a fish eats are more active and fish eat more often.
                            In cold water, it takes a while for a fish meal to be completely digested and utilized and the fish are less inactive.
                            The best water temperature range of different species of fish in different areas it can vary. We can follow this table:</p>
