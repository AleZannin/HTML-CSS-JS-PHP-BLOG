                        <h3 class="post_title text-center mt-3">The Pheasant Tail Nymph </h3>
                         <p class="text mt-1">
Pheasant Tail Nymph (PTN) was invented by the English river keeper Frank Sawyer in 1958. 
It imitates a wide variety of freshwater insects, that's why probably it's really attractive for the trouts. <br>
Used in creek, stream, and river to fish under the water surface, PTN is really productive when it is drag by the water flow close to the bottom.
Since the ’50s there were created lots of different variations in shape and color, but all of them with an essential material: few barbs of pheasant tail feather. <br>
It can be tied in different sizes, from hooks size 16 to 10, and can be weighted with lead wire, brass or tungsten cones and brass or tungsten beads.
In the picture below my own variation, <a href="https://upload.wikimedia.org/wikipedia/commons/8/83/Pheasant_Tail_Nymph.jpg"> here</a> the classic. I tie the PTN in different colors but the olive one is my favorite. I caught lots of trouts and graylings with this pattern.
</p>
                   <img src="img/PTN.jpg" class="img-fluid mt-3" alt="PTN" id="post-pics">
                        <p class="text mt-3"><i>Imitates</i>: mayfly larvae, stonefly, olive nymphs.</p>
                        <p class="text mt-3"><b>Material:</b></p>
                   <ul>
                       <li>Pheasant tail for tail and body</li>
                       <li>Wire</li>
                       <li>Hen, cock or partridge for legs</li>
                       <li>Tungsten bead, it's allowed to sink really fast</li>
                   </ul>
