                        <h1 class="post_title text-center">3 essential flies for trouts you must have in your fly-box</h1>
 <p><i class="far fa-calendar-alt mt-4 ml-3" id="date"> 2th June 2019</i></p>
                        <p class="text mt-3">We are living in an age of massive evolution, in the last decades, the materials development became fast as never before. This evolution brought to us big changes, as we are applying the new discoveries in everyday life we are doing the same with the fly-tying material. <br>
Nowadays surf an online store can be really dispersive, you can be lost in it, be aware of this.
Many different materials doing the same function, or they are slightly different, that’s mean they do the same function. <br>
This large growth in tying materials is certainly good for the fly-tier art, to work on new ideas or a new way to tie, but at the end of the day, just in rare cases, this is useful for fishing. <br>
The old classic patterns, most of the time, pay off much better.
</p>
