                        <h4 class="post_title text-center mt-3">The Woolly Bugger </h4>
                         <p class="text mt-1">Woolly Bugger was invented in 1967 by the American fly-tier Russel Blessing, the origin is unknown but it seems an evolution of the British Palmer (1700’s). <br>
It is tied in different colors, materials, and styles, to imitate an ample variety of fish prey.
Used for fishing under the water surface, the Woolly Bugger is largely used in the stream, creek, river or lake, but it can be productive, tied in the proper colors, even in saltwater. <br>
It can be tied in different sizes, from hooks size 10 to 2, and can be weighted with lead wire, brass or tungsten cones,  brass or tungsten beads, and dumbbell or chains eyes.</p>
                   <img src="img/wollyBugger.jpg" class="img-fluid mt-3" alt="wollyBugger" id="post-pics">
                        <p class="text mt-3"><i>Imitates</i>: baitfish, leeches, large nymphs, clam worms or shrimps.</p>
                        <p class="text mt-3"><b>Material:</b></p>
                   <ul>
                       <li>Marabou feathers</li>
                       <li>Cock hackle</li>
                       <li>Chenille or dubbing</li>
                       <li>Wire</li>
                       <li>Eventually, something to increase weight</li>
                   </ul>
