                        <h4 class="post_title text-center mt-3">The Elk Hair Caddis </h4>
                         <p class="text mt-1">The Elk Hair Caddis was created by the American fly fisherman Al Throth. <br>
The Elk Hair Caddis imitates a small stonefly or adult caddis fly. It is a dry fly, and it is used to fish on the water surface.
Some variations of this popular pattern are without palmered hackle, or with olive or grizzly body.  It is tied with hooks from size 20 to size 10.<br>
The material involved to tie this fly is few and it’s one of the easiest flies to tie.</p>
                   <img src="img/elkHairCaddis.jpg" class="img-fluid mt-3" alt="elkHairCaddis" id="post-pics">
                        <p class="text mt-3"><i>Imitates</i>: caddisflies, stoneflies.</p>
                        <p class="text mt-3"><b>Material:</b></p>
                   <ul>
                       <li>Elk's hair</li>
                       <li>Cock hackle</li>
                       <li>Fine wire</li>
                       <li>Dubbing</li>
                   </ul>
