                        <p class="text mt-3">What do you need:
                        </p>

                        <ol>
                            <li>Hook Size 10 to 18 (<a href="https://www.amazon.co.uk/Fulling-Mill-Tying-Barbless-Choice/dp/B07K9XJ9DY/ref=as_li_ss_tl?dchild=1&keywords=jig+hooks+fly+tying&psc=1&qid=1570833589&sr=8-2&linkCode=ll1&tag=0luresandflie-21&linkId=bddc80e52f753ab92c7555790b8c180c&language=en_GB" target="_blank">Buy it</a> on Amazon)</li>
                            <li>Tungsten Bead (<a href="https://www.amazon.co.uk/Veniard-Countersunk-Tungsten-Coloured-available/dp/B07K7S12NL/ref=as_li_ss_tl?keywords=Veniard+Countersunk+Tungsten+Coloured+Beads,+Fly+Tying+or+Crafting,+Choices+available&qid=1566324390&s=gateway&sr=8-1&linkCode=ll1&tag=0luresandflie-21&linkId=bd14e8b0b27766b9a44c4bfd145448d1&language=en_GB" target="_blank">Buy it</a> on Amazon)</li>
                            <li>Vynil-Rib, it is a translucent half-round material used for segmented body</li>
                            <li>Ice Dubbing (<a href="https://www.amazon.co.uk/Hareline-Dubbing-Ice-Pink-Dubbin/dp/B0155Q4P22/ref=as_li_ss_tl?dchild=1&keywords=ice+dubbing&psc=1&qid=1570833987&sr=8-23&linkCode=ll1&tag=0luresandflie-21&linkId=7c86daa1f2966bb851fdaa6d1012d86b&language=en_GB" target="_blank">Buy it</a> on Amazon)</li>
                        </ol>

                        <img src="img/ny1.jpg" class="img-fluid mt-3" alt="nymph1" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 1</i>: cut a string of Vynil-Rib, hold it between thumb and forefinger. <br>
                            With a lighter slowly heat up the material, it takes ½ second to get warm enough, don't burn it!
                        </p>

                        <img src="img/ny2.jpg" class="img-fluid mt-3" alt="nymph2" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 2</i>: With the thumb and forefinger press the warm part of the Vynil-Rib to make it flat, step 1 and step 2 are made to improve the fly silhouette.</p>

                        <img src="img/ny3.jpg" class="img-fluid mt-3" alt="nymph3" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 3</i>: fix the thread on the hook and wrap it till the hook curve.</p>

                        <img src="img/ny4.jpg" class="img-fluid mt-3" alt="nyhmph4" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 4</i>: Place the pressed part of the Vynil-Rib on the hook and with few nice and strong turns fix the Vinyl-Rib on the hook, try to make a slight V shape with the thread.</p>

                        <img src="img/ny5.jpg" class="img-fluid mt-3" alt="nymph5" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 5</i>: Hold firmly the Vinyl-Rib, pull it and start to wrap it around the hook shank, until the tungsten bead. This step is the most important, make sure the material is not loose, it has to be fixed.
                            Secure it with the thread and trim the excess part.
                        </p>

                        <img src="img/ny6.jpg" class="img-fluid mt-3" alt="nyhmph6" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 6</i>: With the dubbing needle apply some varnish on all the fly, both tungsten bead and Vinyl-Rib body, this cover will make your nymph more durable against stones and trout teeth.
                            Let it dry. <br>
                            (You can use UV resin, better if thin, to make this step faster)
                        </p>

                        <img src="img/ny7.jpg" class="img-fluid mt-3" alt="nyhmph7" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 7</i>: After have waxed the thread put on it some ice dubbing.</p>

                        <img src="img/ny8.jpg" class="img-fluid mt-3" alt="nyhmph8" id="post-pics">
                        <p class="text mt-1 text-center"> <i>Step 8</i>: Wrap it and finish the fly, with the dubbing needle pull out few strings of ice dubbing, they will make some movement in the water. Now you are ready to catch a lot of fish with this fly before throwing it.
                        </p>

                        <img src="img/ny9.jpg" class="img-fluid mt-3" alt="nyhmphs" id="post-pics">
                        <p class="text mt-1 text-center"> Tie it in different colours, they all work well.</p>

                        <p class="text mt-3">This fly is really effective all year round, it worth a try. <br>
                            The Vinyl-Rib translucent property is useful if you want to get different shades. <br>
                            If you tie the same fly using black thread the shade will be darker, on the other hand, if you tie it with white thread the shade will be lighter.
                        </p>
