                        <h3 class="post_title text-center">How to tie a hammer proof nymph for rivers</h3>
                        <p><i class="far fa-calendar-alt mt-4 ml-3" id="date"> 11th October 2019</i></p>
                        <p class="text mt-3">Fishing with nymphs in the river is one of the most common and productive methods used in fly-fishing. <br>
                            You don’t need to wait for the hatch, you can fish all day long and all year round, even in the winter. <br>
                            It is as well much more profitable than dry-fly fishing, trouts get 90% of the food under the water surface. <br><br>
                            The nymph in this post will be quite heavy, this is the reason why it is good for rivers and streams but not for lakes, where you need to use light-weight nymphs to not lose the imitative swimming property. <br>
                            In rivers and stream, sometimes, you have to go down quickly, get the bottom as fast as you can, this nymph is great for this purpose. <br><br>
                            The best colour for the nymph is, without doubt, olive.
                        </p>
                        <img src="img/Mayfly_nymph.jpg" class="img-fluid mt-3" alt="mayflyNymph" id="post-pics">
                        <figcaption class="figure-caption text-center mt-2">MAYFLY NYMPH <br>Photo by <span style="color:blue">Ian Alexander</span> licensed under the <a href="https://creativecommons.org/">Creative Commons</a></figcaption>
                        <p class="text mt-3">Olive colour imitates a wide range of aquatic insects, especially of the Beatis, a genus of mayflies. These species are distributed everywhere, especially in Europe and North America.</p>
