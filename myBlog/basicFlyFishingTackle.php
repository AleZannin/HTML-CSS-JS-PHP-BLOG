                        <h3 class="text-center mt-3"></h3>
                        <p class="text mt-2">WADERS and BOOTS: not strictly necessary, but a good pair of wader let you get in the water.
                            Thanks to the new technology wader keep you warm during the cold season and don’t overheat you during the summer. <br> <br>
                            FORCEPS: necessary when your fly go too deep into the fish, where your fingers can’t reach it, so you can slip the hook out with no problem. <br> <br>
                            LEADER and TIPPET: fluorocarbon or nylon (transparent monofilament). Better get ready a few casts according to the targeted fish, in order to don’t waste time. <br> <br>
                            STRIKE INDICATOR: for nymph fishing, they allow you to control the depth, useful for locating the fish. Do check the fishery rules, if is permitted to fish with more than one fly put on an elk hair caddis, sometimes pay off really well. <br><br>

                            EXTRA: INSECT REPELLANT, almost necessary in some spots. DIFFERENT LINES, for different spots and technics. PACK BAG: another thing almost necessary, especially for long day fishing, you can carry some food, something to drink and extra equipments. PRIEST, do you really need a priest? In case you want to dispatches fish better do it in a humanely way is better use it. Anyway let me know what do you think about catch and release in the comment below.
