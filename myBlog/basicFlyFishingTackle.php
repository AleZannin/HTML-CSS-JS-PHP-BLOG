                            HAT: you can’t go fishing without a hat, better if is a cap with a peak for better protection from the sun, so lets you watch the water easily.
                            Sometimes, especially in a windy day, protect you from your own flies. <br> <br>
                            POLARIZING GLASSES: particularly essential when you fishing sub-surface with wet flies, nymphs or buzzer. Double protection function: <br>
                            1- protect the eyes from your own flies. <br>
                            2- protect against eye strain. <br> <br>
                            FISHING CHEST-PACK OR VEST: essential, you need something where to put all your tools, phone, car key, etc etc etc…
