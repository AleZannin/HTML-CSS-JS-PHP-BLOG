                            During the 18th century in both England and America began the commercial manufacturing of lures, the individual craftsmen work switch to large-scale production.
                            Those artificial baits were largely made by graven wood or kitchen spoons. <br><br>

                       In the mid of the 19th century the number and variety of lures heve grown a lot, from these years in the tackle shop we can find many different shapes, colors and weights. Since then the market has not stopped increasing. <br>
