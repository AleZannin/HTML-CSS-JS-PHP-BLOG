                   <img src="img/squirmyStep1.JPG" class="img-fluid mt-3" alt="squirmyStep1" id="post-pics">
                   <p class="text mt-1 text-center"> <i>Step 1</i>: fix the thread on the hook and wrap it till the hook curve, it is really important to use floss, a normal thread can damage or even cut the rubbery part.</p>
                   
                     <img src="img/squirmyStep2.JPG" class="img-fluid mt-3" alt="squirmyStep2" id="post-pics">
                   <p class="text mt-1 text-center"> <i>Step 2</i>: trim the floss excess and fix the rubber with a loose turn (practice a bit  this step, the floss has to be tie just enough to don’t let the rubber slip away), do another two thighs turns near the first turn but not over the rubber.</p>
                   
                   <img src="img/squirmyStep3.JPG" class="img-fluid mt-3" alt="squirmyStep3" id="post-pics">
                   <p class="text mt-1 text-center"> <i>Step 3</i>: fix the chenille and bring the floss near the tungsten bead.</p>
                   
                   <img src="img/squirmyStep4.JPG" class="img-fluid mt-3" alt="squirmyStep4" id="post-pics">
                   <p class="text mt-1 text-center"> <i>Step 4</i>: stretch gently the rubber and fix it like in step 2.</p>
                   
                   <img src="img/squirmyStep5.JPG" class="img-fluid mt-3" alt="squirmyStep5" id="post-pics">
                   <p class="text mt-1 text-center"> <i>Step 5</i>: Wrap the chenille till the tungsten bead, fix it and finish the Squirmy Wormy with a drop of glue, the Squirmy Wormy is finished.</p>
                   
                   <img src="img/squirmys.JPG" class="img-fluid mt-3" alt="squirmys" id="post-pics">
                   <p class="text mt-1 text-center">Tie it in different sizes and different colours.</p>
                   
                        <p class="text mt-3">The chenille makes the Squirmy Wormy more durable, it covers the part where the trout teeth can break it. <br>
It doesn’t really matter if you lose a piece of rubber in the point or in the tail, it is important to keep this pattern in shape as a real worm as much as possible. <br><br>

How to fish the Squirmy Wormy: <br>
Use it in streams or rivers, let it follows the water flow, don't pull it.
If the water is not too fast work with the rod tip, give some taps to the Squirmy Wormy to imitate the movement of a real worm.
 </p>
