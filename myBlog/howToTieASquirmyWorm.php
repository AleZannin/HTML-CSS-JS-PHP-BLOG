                        <h3 class="post_title text-center">How to tie a durable Squirmy Wormy in few easy steps</h3>
                        <img src="img/squirmy.JPG" class="img-fluid mt-3" alt="squirmy" id="post-pics">
 <p><i class="far fa-calendar-alt mt-4 ml-3" id="date"> 20th August 2019</i></p>
                        <p class="text mt-3">The Squirmy Wormy is a really attractive pattern for trouts, a pattern than can save your day, literally! It is made with few material and is ready in a couple of minutes. <br>
I have tried to tie it in different ways but the part attached to the hook was always easy breakable. <br>
 the Squirmy Wormy is mainly made by a piece of rubber material, this material has great movement in the water but is really tender.
Sun and air can damage it with the time, but, more worst, are trouts teeth, they can break it. <br>
Finally, I found a way to tie it stronger and much more durable. <br><br></p>

